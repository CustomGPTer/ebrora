export interface PlantType {
  id: string;
  name: string;
  checks: string[];
}

export interface FleetItem {
  id: string;
  plantTypeId: string;
  plantNumber: string;
  operatorName: string;
  cpcsNumber: string;
  cpcsExpiry: string;
}

export interface CheckMark {
  /** key: `${checkIndex}-${dayIndex}` */
  [key: string]: "pass" | "fail" | "na" | null;
}

export const DAYS = ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"] as const;

export function genId() {
  return Math.random().toString(36).slice(2, 10);
}

export function createFleetItem(plantTypeId: string): FleetItem {
  return { id: genId(), plantTypeId, plantNumber: "", operatorName: "", cpcsNumber: "", cpcsExpiry: "" };
}

